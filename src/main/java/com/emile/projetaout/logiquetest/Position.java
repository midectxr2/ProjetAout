package com.emile.projetaout.logiquetest;

public class Position {
    private int x;
    private int y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getters pour x et y

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
